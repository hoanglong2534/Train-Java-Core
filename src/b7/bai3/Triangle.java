package b7.bai3;

public class Triangle {
    private long canh1, canh2, canh3;
    public Triangle(long canh1, long canh2, long canh3) {
        this.canh1 = canh1;
        this.canh2 = canh2;
        this.canh3 = canh3;
    }
    public long getCanh1() {
        return canh1;
    }

    public void setCanh1(long canh1) {
        this.canh1 = canh1;
    }

    public long getCanh2() {
        return canh2;
    }

    public void setCanh2(long canh2) {
        this.canh2 = canh2;
    }

    public long getCanh3() {
        return canh3;
    }

    public void setCanh3(long canh3) {
        this.canh3 = canh3;
    }
}
